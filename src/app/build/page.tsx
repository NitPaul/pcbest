"use client";

import { useState, useMemo, useRef } from "react";
import Link from "next/link";
import {
  Wrench, Plus, X, ChevronDown, Download, Share2, Trash2,
  CheckCircle, AlertTriangle, ShoppingCart, ExternalLink, Search,
} from "lucide-react";
import { useData } from "@/components/DataProvider";
import { getBestPrice, formatPrice } from "@/lib/data";
import { Product, COMPONENT_CATEGORIES, SHOP_INFO, ShopName } from "@/lib/types";

interface BuildSlot {
  category: string;
  label: string;
  required: boolean;
  product: Product | null;
}

export default function PCBuilderPage() {
  const { products } = useData();
  const [slots, setSlots] = useState<BuildSlot[]>(
    COMPONENT_CATEGORIES.map((c) => ({
      category: c.key,
      label: c.label,
      required: c.required,
      product: null,
    }))
  );
  const [activeSlot, setActiveSlot] = useState<string | null>(null);
  const [searchQuery, setSearchQuery] = useState("");
  const [buildName, setBuildName] = useState("My Custom Build");
  const buildRef = useRef<HTMLDivElement>(null);

  const availableProducts = useMemo(() => {
    if (!activeSlot) return [];
    let result = products.filter((p) => p.category === activeSlot);
    if (searchQuery) {
      const q = searchQuery.toLowerCase();
      result = result.filter(
        (p) => p.name.toLowerCase().includes(q) || p.brand.toLowerCase().includes(q)
      );
    }
    return result;
  }, [activeSlot, searchQuery]);

  const totalPrice = useMemo(() => {
    return slots.reduce((sum, slot) => {
      if (slot.product) {
        return sum + getBestPrice(slot.product).price;
      }
      return sum;
    }, 0);
  }, [slots]);

  const selectedCount = slots.filter((s) => s.product).length;
  const requiredMissing = slots.filter((s) => s.required && !s.product);

  const selectProduct = (product: Product) => {
    setSlots((prev) =>
      prev.map((s) => (s.category === activeSlot ? { ...s, product } : s))
    );
    setActiveSlot(null);
    setSearchQuery("");
  };

  const removeProduct = (category: string) => {
    setSlots((prev) =>
      prev.map((s) => (s.category === category ? { ...s, product: null } : s))
    );
  };

  const clearBuild = () => {
    setSlots((prev) => prev.map((s) => ({ ...s, product: null })));
    setBuildName("My Custom Build");
  };

  const downloadBuild = () => {
    const lines = [
      `PC Build: ${buildName}`,
      `Generated by PCBest (pcbest.vercel.app)`,
      `Date: ${new Date().toLocaleDateString()}`,
      `${"=".repeat(60)}`,
      "",
    ];

    for (const slot of slots) {
      if (slot.product) {
        const best = getBestPrice(slot.product);
        const shopInfo = SHOP_INFO[best.shop as ShopName];
        lines.push(`${slot.label}: ${slot.product.name}`);
        lines.push(`  Best Price: ${formatPrice(best.price)} at ${shopInfo?.name || best.shop}`);
        lines.push(`  Link: ${slot.product.prices.find((p) => p.shop === best.shop)?.url || ""}`);
        lines.push("");
      }
    }

    lines.push(`${"=".repeat(60)}`);
    lines.push(`Total Build Cost: ${formatPrice(totalPrice)}`);
    lines.push("");
    lines.push("Shop-wise Breakdown:");

    const shopTotals: Record<string, number> = {};
    for (const slot of slots) {
      if (slot.product) {
        for (const price of slot.product.prices.filter((p) => p.inStock)) {
          shopTotals[price.shop] = (shopTotals[price.shop] || 0) + price.price;
        }
      }
    }

    for (const [shop, total] of Object.entries(shopTotals).sort(([, a], [, b]) => a - b)) {
      const shopInfo = SHOP_INFO[shop as ShopName];
      lines.push(`  ${shopInfo?.name || shop}: ${formatPrice(total)}`);
    }

    const blob = new Blob([lines.join("\n")], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `${buildName.replace(/\s+/g, "_")}_PCBest.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  const shareBuild = async () => {
    const slugs = slots
      .filter((s) => s.product)
      .map((s) => `${s.category}=${s.product!.slug}`)
      .join("&");
    const url = `${window.location.origin}/build?${slugs}`;

    if (navigator.share) {
      await navigator.share({ title: buildName, text: `Check out my PC build on PCBest!`, url });
    } else {
      await navigator.clipboard.writeText(url);
      alert("Build link copied to clipboard!");
    }
  };

  return (
    <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
      {/* Header */}
      <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-4 mb-8">
        <div>
          <h1 className="text-2xl font-bold flex items-center gap-2">
            <Wrench className="w-6 h-6 text-primary" />
            PC Builder
          </h1>
          <p className="text-sm text-muted mt-1">
            Select components to build your dream PC with the best prices
          </p>
        </div>
        <div className="flex items-center gap-2">
          <button
            onClick={shareBuild}
            disabled={selectedCount === 0}
            className="flex items-center gap-1.5 px-3 py-2 border border-card-border rounded-lg text-sm hover:bg-muted-bg transition disabled:opacity-50"
          >
            <Share2 className="w-4 h-4" />
            Share
          </button>
          <button
            onClick={downloadBuild}
            disabled={selectedCount === 0}
            className="flex items-center gap-1.5 px-3 py-2 bg-primary text-white rounded-lg text-sm hover:bg-primary-dark transition disabled:opacity-50"
          >
            <Download className="w-4 h-4" />
            Download
          </button>
          <button
            onClick={clearBuild}
            disabled={selectedCount === 0}
            className="flex items-center gap-1.5 px-3 py-2 border border-danger/30 text-danger rounded-lg text-sm hover:bg-danger/5 transition disabled:opacity-50"
          >
            <Trash2 className="w-4 h-4" />
            Clear
          </button>
        </div>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
        {/* Build Slots */}
        <div className="lg:col-span-2 space-y-3" ref={buildRef}>
          {/* Build Name */}
          <div className="bg-card border border-card-border rounded-xl p-4">
            <label className="text-xs font-medium text-muted uppercase tracking-wider">Build Name</label>
            <input
              type="text"
              value={buildName}
              onChange={(e) => setBuildName(e.target.value)}
              className="w-full mt-1 text-lg font-semibold bg-transparent border-none focus:outline-none"
              placeholder="Name your build..."
            />
          </div>

          {/* Component Slots */}
          {slots.map((slot) => (
            <div
              key={slot.category}
              className={`bg-card border rounded-xl p-4 transition ${
                slot.product ? "border-success/30" : slot.required ? "border-warning/30" : "border-card-border"
              }`}
            >
              <div className="flex items-center justify-between">
                <div className="flex items-center gap-3">
                  <div className={`w-10 h-10 rounded-lg flex items-center justify-center text-sm font-bold ${
                    slot.product ? "bg-success/10 text-success" : "bg-muted-bg text-muted"
                  }`}>
                    {slot.product ? <CheckCircle className="w-5 h-5" /> : slot.label.slice(0, 2)}
                  </div>
                  <div>
                    <div className="flex items-center gap-2">
                      <h3 className="font-medium text-sm">{slot.label}</h3>
                      {slot.required && !slot.product && (
                        <span className="text-[10px] text-warning bg-warning/10 px-1.5 py-0.5 rounded font-medium">
                          Required
                        </span>
                      )}
                    </div>
                    {slot.product ? (
                      <p className="text-sm text-muted">{slot.product.name}</p>
                    ) : (
                      <p className="text-xs text-muted">No component selected</p>
                    )}
                  </div>
                </div>

                <div className="flex items-center gap-2">
                  {slot.product && (
                    <>
                      <span className="text-lg font-bold text-primary">
                        {formatPrice(getBestPrice(slot.product).price)}
                      </span>
                      <button
                        onClick={() => removeProduct(slot.category)}
                        className="p-1.5 hover:bg-danger/10 text-muted hover:text-danger rounded-lg transition"
                      >
                        <X className="w-4 h-4" />
                      </button>
                    </>
                  )}
                  <button
                    onClick={() => {
                      setActiveSlot(activeSlot === slot.category ? null : slot.category);
                      setSearchQuery("");
                    }}
                    className={`flex items-center gap-1 px-3 py-1.5 rounded-lg text-sm transition ${
                      activeSlot === slot.category
                        ? "bg-primary text-white"
                        : "bg-muted-bg hover:bg-primary/10 text-muted hover:text-primary"
                    }`}
                  >
                    {slot.product ? "Change" : <><Plus className="w-3.5 h-3.5" /> Add</>}
                  </button>
                </div>
              </div>

              {/* Product Picker */}
              {activeSlot === slot.category && (
                <div className="mt-4 border-t border-card-border pt-4">
                  <div className="relative mb-3">
                    <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted" />
                    <input
                      type="text"
                      value={searchQuery}
                      onChange={(e) => setSearchQuery(e.target.value)}
                      placeholder={`Search ${slot.label.toLowerCase()}...`}
                      className="w-full pl-9 pr-3 py-2 text-sm bg-muted-bg border border-card-border rounded-lg focus:outline-none focus:ring-2 focus:ring-primary/50"
                    />
                  </div>
                  <div className="space-y-2 max-h-64 overflow-y-auto">
                    {availableProducts.length === 0 ? (
                      <p className="text-sm text-muted text-center py-4">No products found in this category</p>
                    ) : (
                      availableProducts.map((p) => {
                        const best = getBestPrice(p);
                        return (
                          <button
                            key={p.id}
                            onClick={() => selectProduct(p)}
                            className="w-full flex items-center gap-3 p-3 rounded-lg hover:bg-muted-bg transition text-left"
                          >
                            <img src={p.image} alt={p.name} className="w-12 h-12 object-contain rounded bg-muted-bg" />
                            <div className="flex-1 min-w-0">
                              <p className="text-sm font-medium truncate">{p.name}</p>
                              <p className="text-xs text-muted">{p.brand}</p>
                            </div>
                            <div className="text-right shrink-0">
                              <p className="font-bold text-primary">{formatPrice(best.price)}</p>
                              <p className="text-xs text-muted">{best.shop}</p>
                            </div>
                          </button>
                        );
                      })
                    )}
                  </div>
                </div>
              )}
            </div>
          ))}
        </div>

        {/* Summary Sidebar */}
        <div className="lg:col-span-1">
          <div className="bg-card border border-card-border rounded-xl p-6 sticky top-20">
            <h2 className="font-bold text-lg mb-4">Build Summary</h2>

            {/* Selected Components */}
            <div className="space-y-3 mb-6">
              {slots.filter((s) => s.product).map((slot) => {
                const best = getBestPrice(slot.product!);
                return (
                  <div key={slot.category} className="flex justify-between items-start text-sm">
                    <div className="min-w-0">
                      <p className="text-muted text-xs">{slot.label}</p>
                      <p className="font-medium truncate">{slot.product!.name}</p>
                    </div>
                    <span className="font-semibold shrink-0 ml-2">{formatPrice(best.price)}</span>
                  </div>
                );
              })}
              {selectedCount === 0 && (
                <p className="text-sm text-muted text-center py-6">
                  No components selected yet. Start adding components to see your build summary.
                </p>
              )}
            </div>

            {/* Warnings */}
            {requiredMissing.length > 0 && selectedCount > 0 && (
              <div className="bg-warning/5 border border-warning/20 rounded-lg p-3 mb-4">
                <p className="text-xs font-medium text-warning flex items-center gap-1">
                  <AlertTriangle className="w-3.5 h-3.5" />
                  Missing required components:
                </p>
                <ul className="text-xs text-muted mt-1 space-y-0.5">
                  {requiredMissing.map((s) => (
                    <li key={s.category}>- {s.label}</li>
                  ))}
                </ul>
              </div>
            )}

            {/* Total */}
            <div className="border-t border-card-border pt-4">
              <div className="flex justify-between items-center">
                <span className="font-medium">Total</span>
                <span className="text-2xl font-bold text-primary">{formatPrice(totalPrice)}</span>
              </div>
              <p className="text-xs text-muted mt-1">
                {selectedCount} of {slots.length} components selected
              </p>
            </div>

            {/* Actions */}
            <div className="mt-4 space-y-2">
              <button
                onClick={downloadBuild}
                disabled={selectedCount === 0}
                className="w-full flex items-center justify-center gap-2 bg-primary hover:bg-primary-dark text-white py-3 rounded-xl font-medium transition disabled:opacity-50"
              >
                <Download className="w-4 h-4" />
                Download Build List
              </button>
              <button
                onClick={shareBuild}
                disabled={selectedCount === 0}
                className="w-full flex items-center justify-center gap-2 border border-card-border hover:bg-muted-bg py-3 rounded-xl font-medium transition disabled:opacity-50 text-sm"
              >
                <Share2 className="w-4 h-4" />
                Share Build
              </button>
            </div>

            {/* Shop Breakdown */}
            {selectedCount > 0 && (
              <div className="mt-6 pt-4 border-t border-card-border">
                <h3 className="text-sm font-medium mb-2">If buying all from one shop:</h3>
                <div className="space-y-1.5">
                  {(() => {
                    const shopTotals: Record<string, { total: number; count: number }> = {};
                    for (const slot of slots) {
                      if (slot.product) {
                        for (const price of slot.product.prices.filter((p) => p.inStock)) {
                          if (!shopTotals[price.shop]) shopTotals[price.shop] = { total: 0, count: 0 };
                          shopTotals[price.shop].total += price.price;
                          shopTotals[price.shop].count += 1;
                        }
                      }
                    }
                    return Object.entries(shopTotals)
                      .filter(([, v]) => v.count === selectedCount)
                      .sort(([, a], [, b]) => a.total - b.total)
                      .slice(0, 5)
                      .map(([shop, { total }]) => {
                        const shopInfo = SHOP_INFO[shop as ShopName];
                        return (
                          <div key={shop} className="flex justify-between items-center text-sm">
                            <span className="text-muted">{shopInfo?.name || shop}</span>
                            <span className="font-medium">{formatPrice(total)}</span>
                          </div>
                        );
                      });
                  })()}
                </div>
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}
